public class LacoFor{
    public static void main(String[] args){
        for(int i = 1; i <=10; i++){
            System.out.println("Número: " + i);
        }
    }
}

// for é usado para repetir um bloco de código um número específico de vezes
// útil quando o número de iterações é conhecido antecipadamente
// a inicialização (int i = 1), condição (i <=10) e incremento (i++) são todos definidos na declaração do for
// pode ser usado para iterar sobre arrays ou coleções, além de contadores simples
